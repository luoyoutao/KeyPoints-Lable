def delete_id_point(id):
    """
    功能 ：删除某个打好的标签
    param id: 该标签所对应的id号
    """
    global flag, text_label_id, text_label_sum, key_id, sums, point_save, coordinate
    x, y = coordinate[id]
    if btn[x][y]['bg'] == 'green':
        btn[x][y].config(bg='red')
    if flag[id] is 1:
        key_id = id
        flag[id] = 0
        sums = sums - 1
        point_save.pop(key_points[id])
        reload()
        text_label_id.set(str(id))
        text_label_sum.set(str(sums))



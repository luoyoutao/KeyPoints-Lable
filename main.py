def save_data_json():
    """
    功能 ：保存s所有打好标签的图像的标签数据和图像信息为json数据
    """
    answer = messagebox.askokcancel(title='友情提示', message='请确保所有的训练图片标签都已经被打好标签再执行，是否继续执行？')
    if answer is False:
        return
    messagebox.showinfo(title='选择文件夹', message='请选择到您之前保存数据的路径，最终的json文件将被保存于该文件夹下')
    save_json_file = filedialog.askdirectory(title='选择一个文件夹', initialdir='./')  # 打开当前程序工作目录
    images_file = save_json_file + '/images.txt'
    annotations_file = save_json_file + '/annotations.txt'
    while (not os.path.exists(images_file) or not os.path.exists(annotations_file)) and answer:
        answer = messagebox.askokcancel(title='友情提示', message='该路径下没有之前保存的数据,是否重新选择？')
        if answer:
            save_json_file = filedialog.askdirectory(title='选择一个文件夹', initialdir='./')
            images_file = save_json_file + '/images.txt'
            annotations_file = save_json_file + '/annotations.txt'
    if answer is False:
        return
    # 下面开始保存数据操作
    images_list = []
    images_txt = open(images_file, 'r')
    lis = images_txt.readlines()
    for i in range(len(lis)):
        images_list.append(eval(lis[i]))
    images_txt.close()
    annotations_list = []
    annotations_txt = open(annotations_file, 'r')
    lis = annotations_txt.readlines()
    for i in range(len(lis)):
        annotations_list.append(eval(lis[i]))
    annotations_txt.close()
    json_data = {
            'images': images_list,
            'annotations': annotations_list,
            'categories': {'key_points': ['nose', 'left_eye', 'right_eye', 'left_ear', 'right_ear', 'left_shoulder',
                                          'right_shoulder', 'left_elbow', 'right_elbow', 'left_wrist', 'right_wrist',
                                          'left_hip', 'right_hip', 'left_knee', 'right_knee', 'left_ankle',
                                          'right_ankle'], 'skeleton': [[16, 14], [14, 12], [17, 15], [15, 13], [12, 13],
                                                                       [6, 12], [7, 13], [6, 7], [6, 8], [7, 9],
                                                                       [8, 10], [9, 11], [2, 3], [1, 2], [1, 3], [2, 4],
                                                                       [3, 5], [4, 6], [5, 7]]}
    }
    js_obj = json.dumps(json_data)  # indent参数是换行和缩进
    file_object = open(save_json_file + '/person_key_points' + '.json', 'w')
    file_object.write(js_obj)
    file_object.close()  # 最终写入的json文件格式



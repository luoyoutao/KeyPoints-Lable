def delete_id_point(id):
    '''
    åŠŸèƒ½ ï¼šåˆ é™¤æŸä¸ªæ‰“å¥½çš„æ ‡ç­¾
    param id: è¯¥æ ‡ç­¾æ‰€å¯¹åº”çš„idå·
    '''
    global flag, text_label_id, text_label_sum, key_id, sums, point_save, coordinate
    x, y = coordinate[id]
    if btn[x][y]['bg'] == 'green':
        btn[x][y].config(bg='grey')
    if flag[id] is 1:
        key_id = id
        flag[id] = 0
        sums = sums - 1
        point_save.pop(key_points[id])

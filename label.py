def delete_id_point(id):
    '''
    功能 ：删除某个打好的标签
    param id: 该标签所对应的id号
    '''
    global flag, text_label_id, text_label_sum, key_id, sums, point_save, coordinate
    x, y = coordinate[id]
    if btn[x][y]['bg'] == 'green':
        btn[x][y].config(bg='grey')
    if flag[id] is 1:
        key_id = id
        flag[id] = 0
        sums = sums - 1
        point_save.pop(key_points[id])
        reload()
        text_label_id.set(str(id))
        text_label_sum.set(str(sums))


def save_data_pic():
    '''
    功能 ：保存当前图像的标签数据和图像信息
    '''
    global image_down, point_save, img_path, file_path_id, sums, key_id, first_save, save_file
    if first_save is 1:
        messagebox.showinfo(title='友情提示', message='请选择一个文件路径，被标记好的图片放在该文件\n路径下的img文件夹中，'
                                                  '标签数据存为该文件路\n径下的images.txt和annotations.txt！')
        save_file = filedialog.askdirectory(title='选择一个文件夹', initialdir='./')  # 打开当前程序工作目录
        if not save_file:
            return
        if not os.path.exists(save_file + '/img'):
            os.mkdir(save_file + '/img')

        first_save = 0
    image_down.save(save_file + '/img/' + img_path.split('/')[-1])  # 保存打好标签后的图片

    file_name_images = save_file + '/images.txt'
    open(file_name_images, 'a')
    image_id = len(open(file_name_images, 'r').readlines())
    json_image = {'image_id': image_id, 'file_name': img_path.split('/')[-1]}
    open(file_name_images, 'a').writelines(json.dumps(json_image) + '\n')
    open(file_name_images, 'a').close()

    file_name_annotations = save_file + '/annotations.txt'
    temp_key_points = []
    for key in key_points[1:]:
        if key in point_save:
            temp_key_points.extend((point_save[key]['x'], point_save[key]['y'], point_save[key]['v']))
        else:
            temp_key_points.extend((0, 0, 0))
    json_annotations = {'image_id': image_id, 'num_key_points': len(point_save), 'key_points': temp_key_points}
    open(file_name_annotations, 'a').writelines(json.dumps(json_annotations) + '\n')
    open(file_name_annotations, 'a').close()

    sums = 0
    key_id = 1

    if len(file_path) > 0 and file_path_id < len(file_path):  # 继续加载后面的即将打标签的图片
        load_many_of_one()


def save_data_json():
    '''
    功能 ：保存s所有打好标签的图像的标签数据和图像信息为json数据
    '''
    answer = messagebox.askokcancel(title='友情提示', message='请确保所有的训练图片标签都已经被打好标签再执行，是否继续执行？')
    if answer is False:
        return
    messagebox.showinfo(title='选择文件夹', message='请选择到您之前保存数据的路径，最终的json文件将被保存于该文件夹下')
    save_json_file = filedialog.askdirectory(title='选择一个文件夹', initialdir='./')  # 打开当前程序工作目录
    images_file = save_json_file + '/images.txt'
    annotations_file = save_json_file + '/annotations.txt'
    while (not os.path.exists(images_file) or not os.path.exists(annotations_file)) and answer:
        answer = messagebox.askokcancel(title='友情提示', message='该路径下没有之前保存的数据,是否重新选择？')
        if answer:
            save_json_file = filedialog.askdirectory(title='选择一个文件夹', initialdir='./')
            images_file = save_json_file + '/images.txt'
            annotations_file = save_json_file + '/annotations.txt'
    if answer is False:
        return
    # 下面开始保存数据操作
    images_list = []
    images_txt = open(images_file, 'r')
    lis = images_txt.readlines()
    for i in range(len(lis)):
        images_list.append(eval(lis[i]))
    images_txt.close()
    annotations_list = []
    annotations_txt = open(annotations_file, 'r')
    lis = annotations_txt.readlines()
    for i in range(len(lis)):
        annotations_list.append(eval(lis[i]))
    annotations_txt.close()
    json_data = {
            'images': images_list,
            'annotations': annotations_list,
            'categories': {'key_points': ['nose', 'left_eye', 'right_eye', 'left_ear', 'right_ear', 'left_shoulder',
                                          'right_shoulder', 'left_elbow', 'right_elbow', 'left_wrist', 'right_wrist',
                                          'left_hip', 'right_hip', 'left_knee', 'right_knee', 'left_ankle',
                                          'right_ankle'], 'skeleton': [[16, 14], [14, 12], [17, 15], [15, 13], [12, 13],
                                                                       [6, 12], [7, 13], [6, 7], [6, 8], [7, 9],
                                                                       [8, 10], [9, 11], [2, 3], [1, 2], [1, 3], [2, 4],
                                                                       [3, 5], [4, 6], [5, 7]]}
    }
    js_obj = json.dumps(json_data)  # indent参数是换行和缩进
    file_object = open(save_json_file + '/person_key_points' + '.json', 'w')
    file_object.write(js_obj)
    file_object.close()  # 最终写入的json文件格式


